class Solution
{
    public:
    Node* reverseBetween(Node* head, int m, int n)
   {
       Node*p= head;
       Node* L1 ,*L2 ,*L3=NULL;
       vector<int>v;
       while(p!=NULL)
       {
           v.push_back(p->data);
           p=p->next;
       }
       
       reverse(v.begin()+m-1,v.begin()+n);
       
        bool flg=0;
       
       
       for(int i=0;i<v.size();i++)
       {
           L1=new Node(v[i]);
           if(flg==0) L3=L1,L2=L1,flg=1;
           else L2->next=L1,L2=L2->next;
       }
       return L3;
   }
};

